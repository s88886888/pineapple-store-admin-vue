import moment from "moment";

const updateTimeFilter = (cellValue:{updateTime:{}})=> {
  
  if (cellValue.updateTime != null) {
    return moment(cellValue.updateTime).format("YYYY-MM-DD HH:MM:ss");
  }
}

  export default updateTimeFilter;
  
